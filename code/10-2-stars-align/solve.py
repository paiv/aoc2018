#!/usr/bin/env python -O
import re


def solve(t, h=10):
    points = [[*map(int, re.findall(r'-?\d+', s))]
        for s in t.splitlines()]

    for t in range(10000000):
        ys = [(y + dy * t) for _,y,_,dy in points]
        if max(ys) - min(ys) == h - 1:
            return t


